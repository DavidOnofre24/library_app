class BookDetailEntity {
  final String title;
  final String subtitle;
  final String authors;
  final String publisher;
  final String isbn13;
  final String pages;
  final String year;
  final String rating;
  final String desc;
  final String price;
  final String image;
  final String url;

  BookDetailEntity({
    required this.title,
    required this.subtitle,
    required this.authors,
    required this.publisher,
    required this.isbn13,
    required this.pages,
    required this.year,
    required this.rating,
    required this.desc,
    required this.price,
    required this.image,
    required this.url,
  });
}
